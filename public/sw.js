// Minimal app-shell service worker (ROADMAP decision 3: installable shell, no
// offline data sync). Network-first for same-origin GETs with a cache fallback,
// so content stays fresh online and the shell still loads offline. Cross-origin
// requests (Supabase, fonts) are never intercepted.
const CACHE = 'verocity-v1';
const SHELL = ['/', '/app', '/login', '/showcase'];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches
      .open(CACHE)
      .then((cache) => cache.addAll(SHELL))
      .then(() => self.skipWaiting()),
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) => Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k))))
      .then(() => self.clients.claim()),
  );
});

self.addEventListener('fetch', (event) => {
  const { request } = event;
  if (request.method !== 'GET') return;
  if (new URL(request.url).origin !== self.location.origin) return;

  event.respondWith(
    fetch(request)
      .then((response) => {
        const copy = response.clone();
        caches.open(CACHE).then((cache) => cache.put(request, copy));
        return response;
      })
      .catch(() => caches.match(request).then((cached) => cached || caches.match('/app'))),
  );
});
